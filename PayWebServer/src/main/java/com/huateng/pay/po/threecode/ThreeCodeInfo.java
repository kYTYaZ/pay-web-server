package com.huateng.pay.po.threecode;

import java.io.Serializable;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlRootElement;

@XmlRootElement
@XmlAccessorType(XmlAccessType.PROPERTY)
public class ThreeCodeInfo implements Serializable{
	
	private static final long serialVersionUID = 1L;
	
	//商户号
	private String merId;
	//交易金额
	private String tradeAmount;
	//交易状态
	private String txnSta;
	//交易状态描述
	private String resDesc;
	//商户订单号
	private String merOrderId;
	//商户订单日期
	private String merOrDt;
	//商户订单时间
	private String merOrTm;
	//商户订单号
	private String txnSeqId;
	//商户订单日期
	private String txnDt;
	//商户订单时间
	private String txnTm;
	//支付时间
	private String settleDate;
	//接入类型
	private String payAccessType;
	//支付类型
	private String payType;
	//渠道号
	private String txnChannel;
	//手续费
	private String fee;
	//订单备注
	private String remark;
	

	public String getMerId() {
		return merId;
	}
	public void setMerId(String merId) {
		this.merId = merId;
	}
	public String getTradeAmount() {
		return tradeAmount;
	}
	public void setTradeAmount(String tradeAmount) {
		this.tradeAmount = tradeAmount;
	}
	public String getTxnSta() {
		return txnSta;
	}
	public void setTxnSta(String txnSta) {
		this.txnSta = txnSta;
	}
	public String getResDesc() {
		return resDesc;
	}
	public void setResDesc(String resDesc) {
		this.resDesc = resDesc;
	}
	public String getMerOrderId() {
		return merOrderId;
	}
	public void setMerOrderId(String merOrderId) {
		this.merOrderId = merOrderId;
	}
	public String getMerOrDt() {
		return merOrDt;
	}
	public void setMerOrDt(String merOrDt) {
		this.merOrDt = merOrDt;
	}
	public String getMerOrTm() {
		return merOrTm;
	}
	public void setMerOrTm(String merOrTm) {
		this.merOrTm = merOrTm;
	}
	public String getSettleDate() {
		return settleDate;
	}
	public void setSettleDate(String settleDate) {
		this.settleDate = settleDate;
	}
	public String getPayAccessType() {
		return payAccessType;
	}
	public void setPayAccessType(String payAccessType) {
		this.payAccessType = payAccessType;
	}
	public String getPayType() {
		return payType;
	}
	public void setPayType(String payType) {
		this.payType = payType;
	}
	public String getTxnSeqId() {
		return txnSeqId;
	}
	public void setTxnSeqId(String txnSeqId) {
		this.txnSeqId = txnSeqId;
	}
	public String getTxnDt() {
		return txnDt;
	}
	public void setTxnDt(String txnDt) {
		this.txnDt = txnDt;
	}
	public String getTxnTm() {
		return txnTm;
	}
	public void setTxnTm(String txnTm) {
		this.txnTm = txnTm;
	}
	public String getTxnChannel() {
		return txnChannel;
	}
	public void setTxnChannel(String txnChannel) {
		this.txnChannel = txnChannel;
	}
	
	public String getFee() {
		return fee;
	}
	public void setFee(String fee) {
		this.fee = fee;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	@Override
	public String toString() {
		
		StringBuffer buf = new StringBuffer("[");
		buf.append("merId=").append(merId).append(",");
		buf.append("tradeAmount=").append(tradeAmount).append(",");
		buf.append("txnSta=").append(txnSta).append(",");
		buf.append("resDesc=").append(resDesc).append(",");
		buf.append("txnSeqId=").append(txnSeqId).append(",");
		buf.append("txnDt=").append(txnDt).append(",");
		buf.append("txnTm=").append(txnTm).append(",");
		buf.append("merOrderId=").append(merOrderId).append(",");
		buf.append("merOrDt=").append(merOrDt).append(",");
		buf.append("merOrTm=").append(merOrTm).append(",");
		buf.append("settleDate=").append(settleDate).append(",");
		buf.append("payAccessType=").append(payAccessType).append(",");
		buf.append("payType=").append(payType);
		buf.append("txnChannel=").append(txnChannel);
		buf.append("fee=").append(fee);
		buf.append("remark=").append(remark).append("]");
		
		return buf.toString();
	}
}

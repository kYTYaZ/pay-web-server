package com.wldk.framework.interceptor;

/**
 * 日志流水记录
 * 
 * @author Administrator
 * 
 */
public class WebLog {
	/** 定义单例 */
	private static WebLog instance = new WebLog();

	/** 当前日志流水号 */
	private String txnSeqId;

	/**
	 * 获取单例对象的方法
	 * 
	 * @return
	 */
	public static WebLog getInstance() {
		return instance;
	}

	public String getTxnSeqId() {
		return txnSeqId;
	}

	public void setTxnSeqId(String txnSeqId) {
		this.txnSeqId = txnSeqId;
	}

}
